def average_of_numbers(num1, num2, num3, num4, num5):
    total = num1 + num2 + num3 + num4 + num5
    average = total/5
    return average 

#example
num1 = 5
num2 = 10
num3 = 15
num4 = 20
num5 = 25
result = average_of_numbers(num1, num2, num3, num4, num5)
print("The average of the 5 numbers is: ", result)
